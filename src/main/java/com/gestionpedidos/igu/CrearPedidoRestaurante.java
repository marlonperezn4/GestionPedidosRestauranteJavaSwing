    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // TODO add your handling code here:
        
        // VALIDAR FORMULARIO
        if(!validarFormulario()){
            return;
        }
        
        // OBTENER DATOS
        String nombre = txtNombrePlato.getText().trim();
        int cantidad = Integer.parseInt( txtCantidadPlatos.getText());
        String direccion = txtDireccionEntrega.getText().trim();
        //OBTENER LA CONFIRMACIÓN DEL PEDIDO
        String metodoEntrega = "";
        if(rbDomicilio.isSelected()){
            metodoEntrega = "Domicilio";
        }else{
            metodoEntrega = "Recogida";
        }
        
        //CREAR OBJETO PEDIDO
        Pedido pedido = new Pedido(
                nombre,
                cmbCategoriaPlato.getSelectedItem().toString(),
                cantidad,
                metodoEntrega,
                direccion
        );
        
        //GUARDAR PEDIDO EN MEMORIA
        Controladora control = new Controladora();
        control.guardarPedido(pedido);
        //MOSTRAR MENSAJE
        JOptionPane.showMessageDialog(this, "Pedido registrado correctamente");
        //LIMPIAR EL FORMULARIO
        limpiarFormulario();
    }//GEN-LAST:event_btnConfirmarActionPerformed

    
