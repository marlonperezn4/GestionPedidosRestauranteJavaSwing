    private boolean validarFormulario() {

        // VALIDACIÓN DEL NOMBRE PLATO
        String nombre = txtNombrePlato.getText().trim();
        if(nombre.isEmpty()){

            JOptionPane.showMessageDialog(this, "El nombre del plato es obligatorio");
            txtNombrePlato.requestFocus();
            return false;
        }
        if(!nombre.matches("[a-zA-Z0-9áéíóúÁÉÍÓÚñÑ ]+")){

            JOptionPane.showMessageDialog(this, "Solo se permiten letras y números en el nombre del plato");
            return false;
        }
        
        //VALIDACIÓN DE LA CATEGORÍA
        if(cmbCategoriaPlato.getSelectedIndex() == 0){

            JOptionPane.showMessageDialog(this,
                    "Seleccione una categoría");

            return false;
        }
        
        //VALIDACIÓN DE LA CANTIDAD
        int cantidad;
        try{

            cantidad = Integer.parseInt(txtCantidadPlatos.getText());

            if(cantidad <= 0){

                JOptionPane.showMessageDialog(this,"La cantidad debe ser positiva");
                return false;
            }

        }catch(NumberFormatException e){

            JOptionPane.showMessageDialog(this,"La cantidad debe ser numérica");
            return false;
        }
        
        //VALIDAR TIPO ENTREGA
        if(!rbDomicilio.isSelected() && !rbRecogida.isSelected()){

             JOptionPane.showMessageDialog(this, "Seleccione método de entrega");

             return false;
        }
        
        //VALIDAR DIRECCIÓN ENTREGA
        String direccion = txtDireccionEntrega.getText().trim();
        if(rbDomicilio.isSelected() && direccion.isEmpty()){

            JOptionPane.showMessageDialog(this,
                    "Ingrese dirección de entrega");

            return false;
        }
        
        return true;
    }

